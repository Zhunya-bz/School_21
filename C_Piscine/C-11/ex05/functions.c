int plus(int a, int b)
{
	return (a + b);
}

int minus(int a, int b)
{
	return (a - b);
}

int multi(int a, int b)
{
	return (a * b);
}

int devide(int a, int b)
{
	return (a / b);
}

int f_module(int a, int b)
{
	return (a % b);
}