int		ft_is_prime(int nb)
{
	int i;
	int s;

	i = 1;
	s = 0;
	if (nb > 1)
	{
		while (i <= nb)
		{
			if ((nb % i) == 0)
				s++;
			i++;
		}
		if (s == 2)
			return (1);
	}
	return (0);
}
